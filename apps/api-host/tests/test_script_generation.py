    provider = object()
    monkeypatch.setenv("SCRIPT_PROVIDER", AiProvider.OPENAI)
    monkeypatch.setattr(factory_module, "LangChainScriptGenerator", lambda: provider)

    result = factory_module.build_script_generator()

    assert result is provider


